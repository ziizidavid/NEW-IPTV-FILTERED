import re
import urllib.request
import hashlib
from pathlib import Path

SOURCE = "https://raw.githubusercontent.com/doms9/iptv/refs/heads/default/M3U8/events.m3u8"

HASH_FILE = ".last_events_hash"
CHANNEL_FILE = "channel"

BEGIN_MARKER = "# --- BEGIN AUTOGENERATED EVENTS ---"
END_MARKER = "# --- END AUTOGENERATED EVENTS ---"

BLOCKED = [
    "cricket",
    "baseball",
    "mlb",
    "nba",
    "wnba",
    "basketball",
    "nfl",
    "american football",
    "nhl",
    "hockey",
    "ufc",
    "mma",
    "boxing",
    "wwe",
    "aew",
    "rugby",
    "formula 1",
    "f1",
    "motogp",
    "nascar",
    "tennis",
    "atp",
    "wta",
    "golf",
    "pga",
    "volleyball",
    "handball",
    "badminton",
    "snooker",
    "darts"
]

def normalize_url(url):
    return url.split("?")[0]

print("Downloading source...")

raw = urllib.request.urlopen(
    SOURCE,
    timeout=30
).read().decode("utf-8", "ignore")

current_hash = hashlib.sha256(
    raw.encode()
).hexdigest()

hash_path = Path(HASH_FILE)

old_hash = ""
if hash_path.exists():
    old_hash = hash_path.read_text().strip()

if current_hash == old_hash:
    print("No changes")
    raise SystemExit(0)

hash_path.write_text(current_hash)

lines = raw.splitlines()

blocks = []
seen_urls = set()

i = 0

while i < len(lines):

    if not lines[i].startswith("#EXTINF"):
        i += 1
        continue

    block = [lines[i]]
    i += 1

    while i < len(lines) and not lines[i].startswith("#EXTINF"):
        block.append(lines[i])
        i += 1

    entry = "\n".join(block)

    text = entry.lower()

    if any(word in text for word in BLOCKED):
        continue

    url_match = re.search(
        r'https?://[^\s]+',
        entry
    )

    if not url_match:
        continue

    url = url_match.group(0)
    url_key = normalize_url(url)

    if url_key in seen_urls:
        continue

    seen_urls.add(url_key)

    entry = re.sub(
        r'group-title="[^"]*"',
        'group-title="Group Event"',
        entry
    )

    blocks.append(entry)

generated = (
    BEGIN_MARKER
    + "\n"
    + "\n\n".join(blocks)
    + "\n"
    + END_MARKER
)

channel = Path(CHANNEL_FILE)

content = channel.read_text(
    encoding="utf-8"
)

if BEGIN_MARKER in content and END_MARKER in content:

    content = re.sub(
        re.escape(BEGIN_MARKER)
        + r".*?"
        + re.escape(END_MARKER),
        generated,
        content,
        flags=re.S,
    )

else:

    content += "\n\n" + generated

channel.write_text(
    content,
    encoding="utf-8"
)

print(f"Updated {len(blocks)} events")
